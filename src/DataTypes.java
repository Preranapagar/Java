
public class DataTypes {
	public static void main(String[] args) {
		/*
		 * Datatypes
		 * 1. Primitive Datatypes
		 *		- byte : used to store number between -128 to 127
		 *		- short : used to store number between -32768 to 32767
		 *		- int : It is used to store number between -2,147,483,648 to 2,147,483,647
		 *		- long :  It is used to store values between -9,223,372,036,854,775,808 to 9,223,372,038,775,807
         *			   : If we have large values then at end of values we use upper case L or lower case l.
		 *		- float : It is used to store the decimal values between 6 to 7 digits
         *				: At the end of values we have use upper case F or lower case f
		 *		- double : It is used to store decimal values upto 15 digits
		 *		- boolean : To store boolean values like True/False
		 *		- char : To store a single character using single quotation
		 *
		 * 2. Non Primitive Datatypes
		 * 		- String
		 * 		- Arrays
		 * 
		 */
		
		//Datatype : byte
		System.out.println("Datatype : byte");
		byte demo = 10;
		byte demo2 = -24;
		
		System.out.println(demo);	
		System.out.println(demo2);
		
		//Datatype : short
		System.out.println("Datatype : short");
		short demo3 = 3492;
		short demo4 = -24500;
		
		System.out.println(demo3);
		System.out.println(demo4);
		
		//Datatype : int
		System.out.println("Datatype : int");
		int demo5 = 34926743;
		int demo6 = -24500572;
		
		System.out.println(demo5);
		System.out.println(demo6);
		
		//Datatype : long
		System.out.println("Datatype : long");
		long a = 190;
		long abk = 1456736742L;
		long xyz = 34542345675223685l;
		
		System.out.println(a);
		System.out.println(abk);
		System.out.println(xyz);
		
		//Datatype : float
		System.out.println("Datatype : float");
		float asd = 56.7f;
		float ask = 56.2F;

		System.out.println(asd);
		System.out.println(ask);
		
		//Datatpe : double
		System.out.println("Datatype : double");
		double ekup = 56.34567852462522;
		
		System.out.println(ekup);
		
		//Datatype : boolean
		System.out.println("Datatype : boolean");
		boolean sat = true;
		
		System.out.println(sat);
		
		//Datatype : char
		System.out.println("Datatype : char");
		char b = 'b';
		
		System.out.println(b);

	}

}
