package StringTutorials;

public class StringLength {

	public static void main(String[] args) {
		// Length Method
		// Use to capture length of string
		// return type of length method is int
		
		String a= "Mumbai";
		
		int b = a.length();
		
		System.out.println(b);
		
		String k= "Mumbai ";
		
		int c = k.length();
		
		System.out.println(c);
				
	}

}
