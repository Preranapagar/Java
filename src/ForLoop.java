
public class ForLoop {

	public static void main(String[] args) {
		/*
		 * Syntax
		 * for(init;condition;updation)
		 * {}
		 */
		
		for(int i=1; i<=10; i++) {
			System.out.println(i);
		}
		
		//while loop : don't know the number of iterations
		// Do while loop : don't know the number of iterations but want to execute the code at least once irrespective of whatever the conditions
		// For Loop : you know the number of iterations

	}

}
