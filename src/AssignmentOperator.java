
public class AssignmentOperator {

	public static void main(String[] args) {
		//AssignmentOperator
		/*
		 * Assignment (=)
		 * Add and Assign (+=)
		 * Sub and Assign (-=)
		 * Div and Assign (/=)
		 * Mul and Assign (*=)
		 * Module and Assign (%=)
		 */
		
		int a = 100;
		System.out.println(a);
		
		a+=2;
		System.out.println(a);
		
		a-=70;
		System.out.println(a);
		
		a/=3;
		System.out.println(a);
		
		a*=6;
		System.out.println(a);
		
		a%=3;
		System.out.println(a);

	}

}
