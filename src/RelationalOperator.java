
public class RelationalOperator {

	public static void main(String[] args) {
		//Relational Operator
		/*
		 * equal to (==)
		 * not equal to (!=)
		 * greater than (>)
		 * greater than equal to (>=)
		 * less than (<)
		 * less than equal to (<=)
		 */
		
		int a = 34;
		int b = 46;
		
		System.out.println(a==b);
		System.out.println(a!=b);
		System.out.println(a>b);
		System.out.println(a>=b);
		System.out.println(a<b);
		System.out.println(a<=b);
		

	}

}
