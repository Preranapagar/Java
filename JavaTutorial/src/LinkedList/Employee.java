package LinkedList;

public class Employee {

	String firstname;

	int age;

	String city;

	// setter method
	public void setFirstName(String newFirstName) {
		
		firstname = newFirstName;
	}
	
	public void setAge(int newAge) {
		age = newAge;
	}
	
	public void setCity(String newCity) {
		city = newCity;
	}
	
	//getter method
	
	public String getFirstName() {
		return firstname;
	}
	
	public int getAge() {
		return age;
	}
	
	public String getCity() {
		return city;
	}
	
}
