package LinkedList;

public class Players {
	
	String playerName;
	
	int age;
	
	public void setPlayerName(String newPlayer) {
		
		playerName = newPlayer;
	}
	
	public void setPlayerAge(int newAge) {
		
		age = newAge;
	}
	
	public String getPlayerName() {
		return playerName;
	}
	
	public int getAge() {
		return age;
	}
}
	
