package Tutorial2;

public class Demo9 {
	public static void main(String[]args)
	{
		int a = 90;
		
		switch(a)
		{
			case 1:
				System.out.println("a value is 1");
				break;
				
			case 10:
				System.out.println("a value is 10");
				break;
				
			case 30:
				System.out.println("a value is 30");
				break;
				
			default:
				System.out.println("Number is not matching");
		}
	}

}
