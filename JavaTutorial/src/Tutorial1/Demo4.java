package Tutorial1;

public class Demo4 {
	public static void main (String[]args) {
		
		int a = 100;
		int b = 20;
		
		System.out.println(a+b);
		
		System.out.println(a-b);
		
		System.out.println(a*b);
		
		System.out.println(a/b);
		
		System.out.println(a%b);
		
		a++;
		
		System.out.println(a);
		System.out.println(a%b);
		
		a--;
		System.out.println(a);
	}

}
