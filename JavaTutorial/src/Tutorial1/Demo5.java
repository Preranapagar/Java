package Tutorial1;

public class Demo5 {
	public static void main(String[]args) {
		 int a = 100;
		 float b = 100.00f;
		 
		//java applied numberic promotion at the time of comparing values

		 System.out.println(a > b);
		 
		 System.out.println(a < b);
		 
		 System.out.println(a == b);
		 
		 System.out.println(a >= b);
		 
		 System.out.println(a <= b);
		 
		 System.out.println(a != b);
	}

}
